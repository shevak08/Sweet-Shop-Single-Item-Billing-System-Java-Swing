    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
 try {
        String product = txtProduct.getText();
        int qty = Integer.parseInt(txtQty.getText());
        int price = Integer.parseInt(txtPrice.getText());

        int totalPrice = qty * price;  // ✅ MULTIPLY

        String Id = txtId.getSelectedItem().toString();

        pst = con.prepareStatement(
            "UPDATE product_tbl SET product=?, price=?, qty=? WHERE id=?"
        );

        pst.setString(1, product);
        pst.setInt(2, totalPrice);  // ✅ TOTAL PRICE
        pst.setInt(3, qty);
        pst.setString(4, Id);

        int k = pst.executeUpdate();

        if (k == 1) {
            JOptionPane.showMessageDialog(this, "Record Updated Successfully!");
            txtProduct.setText("");
            txtPrice.setText("");
            txtQty.setText("");
            FetchData();
            LoadProductNo();
        }

    } catch (SQLException ex) {
        Logger.getLogger(shop1.class.getName()).log(Level.SEVERE, null, ex);
    }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
     String product = txtProduct.getText();

    int qty = Integer.parseInt(txtQty.getText());
    int price = Integer.parseInt(txtPrice.getText());

    int totalPrice = qty * price;   // ✅ MULTIPLICATION

    try {
        pst = con.prepareStatement(
            "INSERT INTO product_tbl(product, price, qty) VALUES (?, ?, ?)"
        );

        pst.setString(1, product);
        pst.setInt(2, totalPrice);  // ✅ STORE TOTAL PRICE
        pst.setInt(3, qty);

        int k = pst.executeUpdate();

        if (k == 1) {
            JOptionPane.showMessageDialog(this, "Record Added Successfully!");
            txtProduct.setText("");
            txtPrice.setText("");
            txtQty.setText("");
            FetchData();
            LoadProductNo();
        } else {
            JOptionPane.showMessageDialog(this, "Record Failed to Save!");
        }

    } catch (SQLException ex) {
        Logger.getLogger(shop1.class.getName()).log(Level.SEVERE, null, ex);
    }
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
    String id = txtId.getSelectedItem().toString();

    try {
        pst = con.prepareStatement("DELETE FROM product_tbl WHERE id=?");
        pst.setString(1, id);

        int k = pst.executeUpdate();
        if (k == 1) {
            JOptionPane.showMessageDialog(this, "Record has been Successfully Deleted!");
            txtProduct.setText("");
            txtPrice.setText("");
            txtQty.setText("");
            txtProduct.requestFocus();
            FetchData();
            LoadProductNo();
        } else {
            JOptionPane.showMessageDialog(this, "Record Failed to Delete!");
        }
    } catch (SQLException ex) {
        Logger.getLogger(shop1.class.getName()).log(Level.SEVERE, null, ex);
        JOptionPane.showMessageDialog(this, "Database error: " + ex.getMessage());
    }
    }//GEN-LAST:event_btnDeleteActionPerformed

