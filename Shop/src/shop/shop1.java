        public void LoadProductNo(){
        try {
            pst = con.prepareStatement("SELECT id FROM product_tbl");
            rs = pst.executeQuery();
            txtId.removeAllItems();
            while(rs.next()){
            txtId.addItem(rs.getString(1));
            }
        }catch (SQLException ex) {
            Logger.getLogger(shop1.class.getName()).log(Level.SEVERE, null, ex);
        }
        }
       private void FetchData(){
        try {
            int q;
            pst =con.prepareStatement("SELECT * FROM product_tbl");
            rs=pst.executeQuery();
            ResultSetMetaData rss = rs.getMetaData();
            q=rss.getColumnCount();
            
            
            DefaultTableModel df = (DefaultTableModel)jTable1.getModel();
            df.setRowCount(0);
            while(rs.next()){
             Vector v2 =new Vector();
             for(int a=1;a<=q;a++){
             v2.add(rs.getString("id"));
               v2.add(rs.getString("product"));
                 v2.add(rs.getString("qty"));
                   v2.add(rs.getString("price"));
             
             }
             df.addRow(v2);
            }
            
        }catch (SQLException ex) {
            Logger.getLogger(shop1.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
